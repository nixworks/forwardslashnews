function byId(id) {
    return new Promise((resolve, reject) => {
        // fake this to return a single object & not an array
        let post = _.filter(data.posts, { ID: Number(id) })[0];
        process.nextTick(
            () => {
                if (post) {
                    resolve(post)
                } else {
                    reject({ error: 'Posts not found' });
                }
            });
    });
}

function find(options) {
    return new Promise((resolve, reject) => {
        var result = null;
        if(options && options.category){
            result = _.filter(data.posts, (post) => {
                return post.categories[options.category];
            });
        }else if(options && options.latest){
            // This could be chained...
            result = _.sortBy(data.posts, function(value) {return new Date(value.date);});
            result = _.takeRight(result, 6);
        }else{
            result = data.posts;
        }
        process.nextTick(
            () => result ? resolve(result) : reject({
                error: 'No posts found',
            }))
    });
}

module.exports = {
    byId, find
}